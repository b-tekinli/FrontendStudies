// Klavye Eventleri



// keypress
document.addEventListener("keypress", run);

function run(e) {
    // console.log(e.which);
    console.log(e.key);
}



// keydown
document.addEventListener("keydown", run);

function run(e) {
    // console.log(e.which);
    console.log(e.key);
}



// keyup
document.addEventListener("keyup", run);

function run(e) {
    // console.log(e.which);
    console.log(e.key);
}
